import 'package:fluent_ui/fluent_ui.dart';

class TextStyleSheet {
  final BuildContext context;
  final Offset position;
  final double scale;
  final double fontWeight;
  final Color color;

  TextStyleSheet({
    required this.context,
    required this.position,
    required this.scale,
    required this.fontWeight,
    required this.color,
  });
}
